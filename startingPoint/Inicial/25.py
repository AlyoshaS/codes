# Leia quatro notas, calcule a média aritmética e imprima o resultado.

# Recebe as notas do usuário
notaA = int(input("Digite o primeira nota: "))
notaB = int(input("Digite o segunda nota: "))
notaC = int(input("Digite a terceira nota: "))
notaD = int(input("Digite a quarta nota: "))

# Calcula a média das notas
soma_das_notas = (notaA + notaB + notaC + notaD)
media_das_notas = soma_das_notas / 4

# Imprime a média das notas
print("A média das notas é de:", media_das_notas)

