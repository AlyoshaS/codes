# Faça a leitura de três valores e apresente como resultado a soma dos quadrados dos três valores lidos

valorA = int(input("Digite o primeiro valor: "))
valorB = int(input("Digite o segundo valor: "))
valorC = int(input("Digite o terceiro valor: "))
A_pot = valorA ** 2
B_pot = valorB ** 2
C_pot = valorC ** 2
soma_dos_quadrados = (A_pot + B_pot + C_pot)
print("A soma dos quadrados dos três valores é de:", soma_dos_quadrados)

