# 08 - Faça um programa que calcule e mostre a área de um triangulo. Sabe-se que: Área = (base * altura) / 2

base = float(input("Digite aqui a base do triangulo: "))
altura = float(input("Digite aqui a altura do triangulo: "))

area = ((base * altura)/2)

print("A área será de: ", area)
